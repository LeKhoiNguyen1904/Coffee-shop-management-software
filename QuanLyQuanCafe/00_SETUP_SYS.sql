PROMPT Buoc 5: Tao profile (mo phong)...
BEGIN
  -- Xoa profile cu neu co
  BEGIN
    EXECUTE IMMEDIATE 'DROP PROFILE CAFE_ADMIN_PROFILE CASCADE';
  EXCEPTION WHEN OTHERS THEN NULL;
  END;
  
  BEGIN
    EXECUTE IMMEDIATE 'DROP PROFILE CAFE_STAFF_PROFILE CASCADE';
  EXCEPTION WHEN OTHERS THEN NULL;
  END;
  
  -- Tao profile moi
  EXECUTE IMMEDIATE 'CREATE PROFILE CAFE_ADMIN_PROFILE LIMIT
    SESSIONS_PER_USER 5
    IDLE_TIME 60
    CONNECT_TIME 480
    PASSWORD_LIFE_TIME 90
    FAILED_LOGIN_ATTEMPTS 5
    PASSWORD_LOCK_TIME 1/24
    PASSWORD_REUSE_TIME 30
    PASSWORD_REUSE_MAX 3';
    
  EXECUTE IMMEDIATE 'CREATE PROFILE CAFE_STAFF_PROFILE LIMIT
    SESSIONS_PER_USER 3
    IDLE_TIME 30
    CONNECT_TIME 240
    PASSWORD_LIFE_TIME 60
    FAILED_LOGIN_ATTEMPTS 3
    PASSWORD_LOCK_TIME 1/24
    PASSWORD_REUSE_TIME 15
    PASSWORD_REUSE_MAX 5';
    
  DBMS_OUTPUT.PUT_LINE(' Da tao 2 profile: CAFE_ADMIN_PROFILE va CAFE_STAFF_PROFILE');
EXCEPTION
  WHEN OTHERS THEN
    DBMS_OUTPUT.PUT_LINE(' Khong the tao profile (co the do quyen): ' || SQLERRM);
    DBMS_OUTPUT.PUT_LINE(' He thong van hoat dong binh thuong, chi khong co profile');
END;
/

