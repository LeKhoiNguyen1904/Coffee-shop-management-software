-- Procedure tao tai khoan
CREATE OR REPLACE PROCEDURE tao_tai_khoan(
  p_username IN VARCHAR2,
  p_manv IN NUMBER,
  p_password IN VARCHAR2,
  p_role_code IN VARCHAR2
) AS
  v_salt RAW(64);
  v_hash RAW(2000);
  v_password_strength VARCHAR2(20);
BEGIN
  -- Kiem tra do manh mat khau
  v_password_strength := pkg_crypto.check_password_strength(p_password);
  IF v_password_strength = 'WEAK' THEN
    RAISE_APPLICATION_ERROR(-20001, 'Mat khau qua yeu. Can it nhat 8 ky tu, chu hoa, chu thuong, so va ky tu dac biet.');
  END IF;
  
  v_salt := pkg_crypto.gen_random_raw(32);
  v_hash := pkg_crypto.hash_password(p_password, v_salt);
  
  INSERT INTO TAIKHOAN(USERNAME, MANV, PASSWORD_HASH, SALT, ROLE_CODE, LOGIN_FAILS, CREATED_AT)
  VALUES (p_username, p_manv, v_hash, v_salt, p_role_code, 0, SYSDATE);
  
  COMMIT;
  
  DBMS_OUTPUT.PUT_LINE('Tao tai khoan thanh cong: ' || p_username || ' - Do manh mat khau: ' || v_password_strength);
END;
/

