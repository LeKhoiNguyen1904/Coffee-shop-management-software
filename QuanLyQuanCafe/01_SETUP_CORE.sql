  PRAGMA AUTONOMOUS_TRANSACTION;
  v_salt RAW(64);
  v_hash RAW(2000);
  v_stored RAW(2000);
  v_locked_until DATE;
  v_fails NUMBER;
  v_role VARCHAR2(30);
  v_manv NUMBER;
  v_password_expired CHAR(1);
BEGIN
  SELECT SALT, PASSWORD_HASH, LOCKED_UNTIL, LOGIN_FAILS, ROLE_CODE, MANV, 
         CASE WHEN PASSWORD_EXPIRES_AT < SYSDATE THEN 'Y' ELSE 'N' END
    INTO v_salt, v_stored, v_locked_until, v_fails, v_role, v_manv, v_password_expired
    FROM TAIKHOAN WHERE USERNAME = p_username;

  IF v_locked_until IS NOT NULL AND v_locked_until > SYSDATE THEN
    INSERT INTO LICHSU_DANGNHAP(USERNAME, MANV, HOATDONG, THOIGIAN, IP_ADDRESS, GHI_CHU)
    VALUES (p_username, v_manv, 'LOGIN_FAIL', SYSDATE, p_ip, 'Tai khoan bi khoa');
    COMMIT;
    RETURN 'LOCKED';
  END IF;
  
  -- Kiem tra password het han
  IF v_password_expired = 'Y' THEN
    INSERT INTO LICHSU_DANGNHAP(USERNAME, MANV, HOATDONG, THOIGIAN, IP_ADDRESS, GHI_CHU)
    VALUES (p_username, v_manv, 'LOGIN_FAIL', SYSDATE, p_ip, 'Mat khau da het han');
    COMMIT;
    RETURN 'EXPIRED';
  END IF;

  v_hash := pkg_crypto.hash_password(p_password, v_salt);

  IF v_hash = v_stored THEN
    UPDATE TAIKHOAN SET LAST_LOGIN_AT = SYSDATE, LOGIN_FAILS = 0, LOCKED_UNTIL = NULL 
    WHERE USERNAME = p_username;
    
    INSERT INTO LICHSU_DANGNHAP(USERNAME, MANV, HOATDONG, THOIGIAN, IP_ADDRESS, GHI_CHU)
    VALUES (p_username, v_manv, 'LOGIN_SUCCESS', SYSDATE, p_ip, 'Dang nhap thanh cong');
    
    COMMIT;
    RETURN 'OK:' || v_role;
  ELSE
    v_fails := v_fails + 1;
    UPDATE TAIKHOAN SET LOGIN_FAILS = v_fails WHERE USERNAME = p_username;
    
    INSERT INTO LICHSU_DANGNHAP(USERNAME, MANV, HOATDONG, THOIGIAN, IP_ADDRESS, GHI_CHU)
    VALUES (p_username, v_manv, 'LOGIN_FAIL', SYSDATE, p_ip, 'Sai mat khau');
    
    IF v_fails >= 5 THEN
      UPDATE TAIKHOAN SET LOCKED_UNTIL = SYSDATE + (15/1440) WHERE USERNAME = p_username;
    END IF;
    
    COMMIT;
    RETURN 'FAIL';
  END IF;
EXCEPTION
  WHEN NO_DATA_FOUND THEN
    COMMIT;
    RETURN 'NOUSER';
END;
/

-- Procedure thay doi mat khau
CREATE OR REPLACE PROCEDURE thay_doi_mat_khau(
  p_username IN VARCHAR2,
  p_old_password IN VARCHAR2,
  p_new_password IN VARCHAR2
) AS
  v_salt RAW(64);
  v_stored_hash RAW(2000);
  v_new_hash RAW(2000);
  v_password_strength VARCHAR2(20);
BEGIN
  -- Lay thong tin hien tai
  SELECT SALT, PASSWORD_HASH INTO v_salt, v_stored_hash
  FROM TAIKHOAN WHERE USERNAME = p_username;
  
  -- Kiem tra mat khau cu
  IF pkg_crypto.hash_password(p_old_password, v_salt) != v_stored_hash THEN
    RAISE_APPLICATION_ERROR(-20002, 'Mat khau cu khong chinh xac');
  END IF;
  
  -- Kiem tra do manh mat khau moi
  v_password_strength := pkg_crypto.check_password_strength(p_new_password);
  IF v_password_strength = 'WEAK' THEN
    RAISE_APPLICATION_ERROR(-20003, 'Mat khau moi qua yeu');
  END IF;
  
  -- Tao salt moi
  v_salt := pkg_crypto.gen_random_raw(32);
  v_new_hash := pkg_crypto.hash_password(p_new_password, v_salt);
  
  -- Cap nhat
  UPDATE TAIKHOAN 
  SET PASSWORD_HASH = v_new_hash,
      SALT = v_salt,
      PASSWORD_CHANGED_AT = SYSDATE,
      PASSWORD_EXPIRES_AT = SYSDATE + 90,
      LOGIN_FAILS = 0,
      LOCKED_UNTIL = NULL
  WHERE USERNAME = p_username;
  
  COMMIT;
  
  DBMS_OUTPUT.PUT_LINE('Doi mat khau thanh cong. Do manh: ' || v_password_strength);
END;
/

