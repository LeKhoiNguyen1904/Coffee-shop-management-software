CREATE OR REPLACE PACKAGE BODY pkg_hoadon AS
  FUNCTION tao_hoadon(
    p_manv IN NUMBER, 
    p_maban IN NUMBER, 
    p_makh IN NUMBER
  ) RETURN NUMBER IS
    v_mahd NUMBER;
  BEGIN
    v_mahd := seq_hoadon.NEXTVAL;
    INSERT INTO HOADON(MAHD, MANV, MABAN, MAKH, NGAYLAP, TONGTIEN, TRANGTHAI)
    VALUES (v_mahd, p_manv, p_maban, p_makh, SYSDATE, 0, 'CHO');
    UPDATE BAN SET TRANGTHAI = 'DANG_SU_DUNG' WHERE MABAN = p_maban;
    COMMIT;
    RETURN v_mahd;
  END;

  PROCEDURE them_cthd(p_mahd NUMBER, p_mamon NUMBER, p_soluong NUMBER, p_dongia NUMBER, p_giamgia NUMBER := 0) IS
    v_thanhtien NUMBER;
  BEGIN
    v_thanhtien := (p_soluong * p_dongia) - p_giamgia;
    INSERT INTO CHITIET_HD(MAHD, MAMON, SOLUONG, DONGIA, GIAMGIA, THANHTIEN)
    VALUES (p_mahd, p_mamon, p_soluong, p_dongia, p_giamgia, v_thanhtien);
    COMMIT;
  END;

  PROCEDURE cap_nhat_tongtien(p_mahd NUMBER) IS
    v_tong NUMBER;
  BEGIN
    SELECT SUM(THANHTIEN) INTO v_tong FROM CHITIET_HD WHERE MAHD = p_mahd;
    UPDATE HOADON SET TONGTIEN = NVL(v_tong, 0) WHERE MAHD = p_mahd;
    COMMIT;
  END;

  PROCEDURE thanh_toan(p_mahd NUMBER) IS
    v_maban NUMBER;
  BEGIN
    SELECT MABAN INTO v_maban FROM HOADON WHERE MAHD = p_mahd;
    UPDATE HOADON SET TRANGTHAI = 'DA_THANH_TOAN' WHERE MAHD = p_mahd;
    UPDATE BAN SET TRANGTHAI = 'TRONG' WHERE MABAN = v_maban;
    COMMIT;
  END;
  
  FUNCTION generate_hoadon_report(p_from_date DATE, p_to_date DATE) RETURN SYS_REFCURSOR IS
    v_cursor SYS_REFCURSOR;
  BEGIN
    OPEN v_cursor FOR
    SELECT 
      MAHD,
      MANV,
      MABAN,
      MAKH,
      NGAYLAP,
      TONGTIEN,
      TRANGTHAI
    FROM HOADON
    WHERE NGAYLAP BETWEEN p_from_date AND p_to_date
    ORDER BY NGAYLAP DESC;
    
    RETURN v_cursor;
  END;
END pkg_hoadon;
