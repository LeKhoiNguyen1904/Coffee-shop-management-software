SELECT h.MAHD, h.MANV, h.MABAN, h.MAKH, h.NGAYLAP, h.TONGTIEN, h.TRANGTHAI,
       nv.HOTEN AS TEN_NHANVIEN,
       b.TENBAN,
       kh.HOTEN AS TEN_KHACHHANG,
