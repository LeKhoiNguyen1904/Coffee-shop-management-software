CREATE OR REPLACE VIEW v_doanhthu_ngay AS
SELECT 
  TRUNC(NGAYLAP) AS NGAY,
  COUNT(*) AS SO_HOA_DON,
  SUM(TONGTIEN) AS DOANH_THU,
  ROUND(AVG(TONGTIEN), 2) AS TRUNG_BINH_HD
FROM HOADON
WHERE TRANGTHAI = 'DA_THANH_TOAN'
GROUP BY TRUNC(NGAYLAP)
ORDER BY NGAY DESC;
/

-- View mon ban chay
CREATE OR REPLACE VIEW v_mon_ban_chay AS
SELECT 
  m.MAMON,
  m.TENMON,
  m.LOAIMON,
  COUNT(ct.MAHD) AS SO_LAN_BAN,
  SUM(ct.SOLUONG) AS TONG_SO_LUONG,
  SUM(ct.THANHTIEN) AS TONG_DOANH_THU,
  ROUND(AVG(ct.DONGIA), 2) AS GIA_TRUNG_BINH
FROM MON m
JOIN CHITIET_HD ct ON m.MAMON = ct.MAMON
JOIN HOADON h ON ct.MAHD = h.MAHD
WHERE h.TRANGTHAI = 'DA_THANH_TOAN'
GROUP BY m.MAMON, m.TENMON, m.LOAIMON
ORDER BY TONG_DOANH_THU DESC;
/

-- View hoa don cua nhan vien
