CREATE OR REPLACE PACKAGE pkg_crypto AS
  FUNCTION gen_random_raw(p_length NUMBER) RETURN RAW;
  FUNCTION aes_encrypt(p_text VARCHAR2, p_key RAW, p_iv RAW) RETURN RAW;
  FUNCTION aes_decrypt(p_encrypted RAW, p_key RAW, p_iv RAW) RETURN VARCHAR2;
  FUNCTION hash_password(p_password VARCHAR2, p_salt RAW) RETURN RAW;
  FUNCTION check_password_strength(p_password VARCHAR2) RETURN VARCHAR2;
END pkg_crypto;
/

CREATE OR REPLACE PACKAGE BODY pkg_crypto AS
  FUNCTION gen_random_raw(p_length NUMBER) RETURN RAW IS
    v_random RAW(2000);
  BEGIN
    v_random := SYS.DBMS_CRYPTO.RANDOMBYTES(p_length);
    RETURN v_random;
  END;

  FUNCTION aes_encrypt(p_text VARCHAR2, p_key RAW, p_iv RAW) RETURN RAW IS
  BEGIN
    RETURN SYS.DBMS_CRYPTO.ENCRYPT(
      UTL_RAW.CAST_TO_RAW(p_text),
      SYS.DBMS_CRYPTO.ENCRYPT_AES256 + SYS.DBMS_CRYPTO.CHAIN_CBC + SYS.DBMS_CRYPTO.PAD_PKCS5,
      p_key, p_iv
    );
  END;

  FUNCTION aes_decrypt(p_encrypted RAW, p_key RAW, p_iv RAW) RETURN VARCHAR2 IS
  BEGIN
    RETURN UTL_RAW.CAST_TO_VARCHAR2(
      SYS.DBMS_CRYPTO.DECRYPT(
        p_encrypted,
        SYS.DBMS_CRYPTO.ENCRYPT_AES256 + SYS.DBMS_CRYPTO.CHAIN_CBC + SYS.DBMS_CRYPTO.PAD_PKCS5,
        p_key, p_iv
      )
    );
  END;
  
  FUNCTION hash_password(p_password VARCHAR2, p_salt RAW) RETURN RAW IS
  BEGIN
    RETURN SYS.DBMS_CRYPTO.HASH(
      UTL_RAW.CAST_TO_RAW(p_password || RAWTOHEX(p_salt)),
      SYS.DBMS_CRYPTO.HASH_SH256
    );
  END;
  
  FUNCTION check_password_strength(p_password VARCHAR2) RETURN VARCHAR2 IS
    v_score NUMBER := 0;
  BEGIN
    -- Ki?m tra ?? dŕi
    IF LENGTH(p_password) >= 8 THEN v_score := v_score + 1; END IF;
    
    -- Có ch? hoa
    IF REGEXP_LIKE(p_password, '[A-Z]') THEN v_score := v_score + 1; END IF;
    
    -- Có ch? th??ng
    IF REGEXP_LIKE(p_password, '[a-z]') THEN v_score := v_score + 1; END IF;
    
    -- Có s?
    IF REGEXP_LIKE(p_password, '[0-9]') THEN v_score := v_score + 1; END IF;
    
    -- Có ký t? ??c bi?t
    IF REGEXP_LIKE(p_password, '[^A-Za-z0-9]') THEN v_score := v_score + 1; END IF;
    
    -- ?ánh giá
    IF v_score >= 4 THEN
      RETURN 'STRONG';
    ELSIF v_score >= 3 THEN
      RETURN 'MEDIUM';
    ELSE
      RETURN 'WEAK';
    END IF;
  END;
END pkg_crypto;
